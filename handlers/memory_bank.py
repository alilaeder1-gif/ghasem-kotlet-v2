def get_few_shot(category: str, count: int = 3) -> list:
    data = FEW_SHOT_DATASET.get(category, [])
    import random
    return random.sample(data, min(count, len(data)))


def get_emotion(emotion: str, count: int = 3) -> list:
    data = EMOTION_DATASET.get(emotion, [])
    import random
    return random.sample(data, min(count, len(data)))


def get_random_proverb() -> str:
    import random
    return random.choice(PROVERBS)


def get_random_expression() -> str:
    import random
    return random.choice(EXPRESSIONS_1400S + TEHRANI_EXPRESSIONS)


def format_few_shot_for_prompt(category: str, count: int = 3) -> str:
    examples = get_few_shot(category, count)
    lines = [f"# {category}"]
    for user, bot in examples:
        lines.append(f"كاربر: {user}")
        lines.append(f"جواب: {bot}")
    return "\n".join(lines)


def format_emotion_for_prompt(emotion: str, count: int = 3) -> str:
    examples = get_emotion(emotion, count)
    return f"# نمونه‌های {emotion}\n" + "\n".join(examples[:count])


def build_behavior_instruction() -> str:
    lines = ["## درخت رفتار"]
    for condition, action in BEHAVIOR_TREE.items():
        lines.append(f"- {condition} → {action}")
    return "\n".join(lines)
